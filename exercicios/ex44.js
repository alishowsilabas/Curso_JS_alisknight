function nomeDoMes(mes){
    switch(mes){
    case 1:
        return 'Mês de Janeiro';
    case 2:
        return 'Mês de Fevereiro';
    case 3:
        return 'Mês de Março';
    case 4:
        return 'Mês de Abril';
    case 5:
        return 'Mês de Maio';
    case 6:
        return 'Mês de Junho';
    case 7:
        return  'Mês de Julho';
     case 8:
        return 'Mês de Agosto';
    case 9:
        return 'Mês de Setembro';
    case 10:
        return'Mês de Outubro';
    case 11:
        return 'Mês de Novembro';
    case 12:
        return 'Mês de Dezembro';
    }
}

